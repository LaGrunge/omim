  LanguageDetector languageDetector;
  for (auto const & word : words)
  {
    auto const & result = languageDetector.DetectLanguage(word);
    std::cerr << word << " language: " << result.m_result << std::endl;
  }


  LanguageDetector2 languageDetector2;
  for (auto const & word : words)
  {
    auto const & result = languageDetector2.DetectLanguage(word);
    std::cerr << word << " language2: " << langs[result.m_language] << std::endl;
  }



